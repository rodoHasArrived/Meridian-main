#!/usr/bin/env python3
"""
validate_budget.py — CI allocation-budget enforcement script.

Reads a ``perf-budgets.json`` file generated by PerformanceBudgetRegistry.ExportJson()
and compares actual BenchmarkDotNet results against the declared budgets.

Usage
-----
    python3 build/scripts/validate_budget.py \\
        --bdn-results ./BenchmarkDotNet.Artifacts/current/results \\
        --budget-json ./BenchmarkDotNet.Artifacts/current/perf-budgets.json \\
        [--fail-on-violation]

Exit codes
----------
    0  — all benchmarks within budget (or --fail-on-violation not set)
    1  — one or more violations when --fail-on-violation is set
    2  — input file not found or JSON parse error
"""

from __future__ import annotations

import argparse
import json
import os
import sys
from dataclasses import dataclass
from pathlib import Path


# ---------------------------------------------------------------------------
# Data types
# ---------------------------------------------------------------------------

@dataclass(frozen=True)
class Budget:
    stage_name: str
    max_allocated_bytes_per_event: int
    max_mean_nanos_per_event: int
    requires_simd: bool


@dataclass(frozen=True)
class BdnResult:
    method_name: str
    mean_ns: float
    allocated_bytes: int


@dataclass(frozen=True)
class BudgetViolation:
    stage_name: str
    budget: Budget
    result: BdnResult
    allocated_over_by: int
    mean_over_by: float


# ---------------------------------------------------------------------------
# Core functions
# ---------------------------------------------------------------------------

def load_budgets(budget_json_path: str) -> dict[str, Budget]:
    """
    Load budgets from the perf-budgets.json generated by
    PerformanceBudgetRegistry.ExportJson().

    Returns a mapping of {stage_name: Budget}.
    """
    path = Path(budget_json_path)
    if not path.exists():
        print(f"[validate_budget] ERROR: budget file not found: {budget_json_path}", file=sys.stderr)
        sys.exit(2)

    try:
        raw = json.loads(path.read_text(encoding="utf-8"))
    except json.JSONDecodeError as exc:
        print(f"[validate_budget] ERROR: could not parse {budget_json_path}: {exc}", file=sys.stderr)
        sys.exit(2)

    budgets: dict[str, Budget] = {}
    for entry in raw:
        b = Budget(
            stage_name=entry["stage_name"],
            max_allocated_bytes_per_event=entry["max_allocated_bytes_per_event"],
            max_mean_nanos_per_event=entry["max_mean_nanos_per_event"],
            requires_simd=entry.get("requires_simd", False),
        )
        budgets[b.stage_name] = b
    return budgets


def load_bdn_results(results_dir: str) -> list[BdnResult]:
    """
    Scan *results_dir* for BDN JSON result files (``*-report-full.json`` or
    ``*-report.json``) and return a flat list of per-benchmark rows.

    BDN full-report schema (simplified):
        { "Benchmarks": [ { "FullName": "...", "Statistics": { "Mean": ... },
                             "Memory": { "BytesAllocatedPerOperation": ... } }, ... ] }
    """
    results_path = Path(results_dir)
    if not results_path.exists():
        print(f"[validate_budget] WARNING: results directory not found: {results_dir}", file=sys.stderr)
        return []

    rows: list[BdnResult] = []

    # Prefer full reports; fall back to standard reports
    json_files = list(results_path.glob("*-report-full.json"))
    if not json_files:
        json_files = list(results_path.glob("*-report.json"))

    for jf in json_files:
        try:
            data = json.loads(jf.read_text(encoding="utf-8"))
        except (json.JSONDecodeError, OSError):
            continue

        for bench in data.get("Benchmarks", []):
            method_name: str = bench.get("FullName") or bench.get("Method") or ""
            stats = bench.get("Statistics") or {}
            memory = bench.get("Memory") or {}

            mean_ns: float = stats.get("Mean", 0.0)
            allocated_bytes: int = memory.get("BytesAllocatedPerOperation", 0)

            rows.append(BdnResult(
                method_name=method_name,
                mean_ns=mean_ns,
                allocated_bytes=allocated_bytes,
            ))

    return rows


def check_budgets(budgets: dict[str, Budget], results: list[BdnResult]) -> list[BudgetViolation]:
    """
    Compare each result against its matching budget entry.

    Matching uses a two-pass strategy:
    1. Full normalised stage_name as a substring of the normalised method name.
    2. Token fallback: any ``_``-delimited token of the stage_name (length ≥ 5) that
       appears case-insensitively in the method name.  This handles cases where stage
       names use abbreviated terms that differ from the BDN class/method names
       (e.g. "DedupKey" vs "DeduplicationKey").

    SIMD entries are skipped.  If no result matches a budget, it is not reported as a
    violation (the benchmark simply may not have run on this CI run).

    Returns a list of violations (empty = all within budget).
    """
    violations: list[BudgetViolation] = []

    for stage_name, budget in budgets.items():
        if budget.requires_simd:
            continue  # SIMD benchmarks excluded from CI gate

        # Find the best (lowest-allocated) matching result row
        matching = [r for r in results if _stage_matches_method(stage_name, r.method_name)]
        if not matching:
            continue  # No result for this budget — not a violation (benchmark may not have run)

        # Use the row with lowest allocation (most optimistic reading)
        best = min(matching, key=lambda r: r.allocated_bytes)

        alloc_over = best.allocated_bytes - budget.max_allocated_bytes_per_event
        mean_over = best.mean_ns - budget.max_mean_nanos_per_event

        if alloc_over > 0 or (budget.max_mean_nanos_per_event < 2**63 - 1 and mean_over > 0):
            violations.append(BudgetViolation(
                stage_name=stage_name,
                budget=budget,
                result=best,
                allocated_over_by=max(0, alloc_over),
                mean_over_by=max(0.0, mean_over),
            ))

    return violations


def _stage_matches_method(stage_name: str, method_name: str) -> bool:
    """
    Return ``True`` if *stage_name* is considered to correspond to *method_name*.

    Pass 1: full normalised stage_name appears as a substring of the normalised
    method name (handles most well-named cases).
    Pass 2: token fallback — any ``_``-delimited token of *stage_name* with length ≥ 5
    that appears case-insensitively in the normalised method name.  Needed when stage
    names use abbreviations different from BDN class/method names.
    """
    norm_method = _normalise(method_name)
    if _normalise(stage_name) in norm_method:
        return True
    tokens = [t.lower() for t in stage_name.split("_") if len(t) >= 5]
    return any(t in norm_method for t in tokens)


def render_summary(violations: list[BudgetViolation], budgets: dict[str, Budget]) -> str:
    """
    Render a Markdown summary table suitable for appending to ``$GITHUB_STEP_SUMMARY``.
    Always emits a table with all non-SIMD budgets; violations are highlighted with ❌.
    """
    lines: list[str] = []
    lines.append("## Performance Budget Report\n")

    violation_names = {v.stage_name for v in violations}

    lines.append("| Stage | Budget (bytes/event) | Actual (bytes/event) | Status |")
    lines.append("|-------|---------------------|---------------------|--------|")

    for stage_name, budget in sorted(budgets.items()):
        if budget.requires_simd:
            lines.append(f"| `{stage_name}` | {budget.max_allocated_bytes_per_event} | _SIMD — excluded_ | ⚠️ SIMD |")
            continue

        violation = next((v for v in violations if v.stage_name == stage_name), None)
        if violation:
            actual = violation.result.allocated_bytes
            status = f"❌ +{violation.allocated_over_by} bytes over budget"
        else:
            actual = "—"
            status = "✅ Pass"

        lines.append(f"| `{stage_name}` | {budget.max_allocated_bytes_per_event} | {actual} | {status} |")

    if violations:
        lines.append(f"\n**{len(violations)} violation(s) detected.**\n")
    else:
        lines.append("\n**All budgets within limits.** ✅\n")

    return "\n".join(lines)


def _normalise(s: str) -> str:
    return s.lower().replace("_", "").replace(".", "").replace(" ", "")


# ---------------------------------------------------------------------------
# CLI entry point
# ---------------------------------------------------------------------------

def main() -> int:
    parser = argparse.ArgumentParser(
        description="Validate BDN benchmark results against declared allocation budgets.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__,
    )
    parser.add_argument(
        "--bdn-results",
        required=True,
        metavar="DIR",
        help="Directory containing BDN JSON result files (e.g. ./BenchmarkDotNet.Artifacts/current/results)",
    )
    parser.add_argument(
        "--budget-json",
        required=True,
        metavar="FILE",
        help="Path to perf-budgets.json generated by PerformanceBudgetRegistry.ExportJson()",
    )
    parser.add_argument(
        "--fail-on-violation",
        action="store_true",
        default=False,
        help="Exit with code 1 if any budget is exceeded",
    )

    args = parser.parse_args()

    budgets = load_budgets(args.budget_json)
    results = load_bdn_results(args.bdn_results)
    violations = check_budgets(budgets, results)

    summary = render_summary(violations, budgets)
    print(summary)

    # Append to GitHub Actions step summary when running in CI
    github_summary = os.environ.get("GITHUB_STEP_SUMMARY")
    if github_summary:
        try:
            with open(github_summary, "a", encoding="utf-8") as f:
                f.write(summary + "\n")
        except OSError:
            pass  # Non-fatal if we can't write the summary

    if violations and args.fail_on_violation:
        print(f"\n[validate_budget] FAILED: {len(violations)} budget violation(s). See table above.", file=sys.stderr)
        return 1

    return 0


if __name__ == "__main__":
    sys.exit(main())
